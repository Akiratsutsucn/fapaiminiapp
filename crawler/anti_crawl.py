def random_ua() -> str:
    return random.choice(UA_POOL)


async def random_sleep(min_s: float = 1.0, max_s: float = 3.0) -> None:
    delay = random.uniform(min_s, max_s)
    logger.debug(f"Sleeping {delay:.2f}s")
    await asyncio.sleep(delay)


